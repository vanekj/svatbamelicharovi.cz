// Load galleries images
$('[data-gallery').each(function() {
	let $gallery = $(this),
		$placeholder = $gallery.find('.js-gallery-placeholder'),
		galleryName = $gallery.attr('data-gallery'),
		galleryUrl = $gallery.attr('data-url'),
		galleryImages = images[galleryName];
	for (let index = 0; index < 42; index++) {
		let randomIndex = Math.floor(Math.random() * galleryImages.length),
			galleryImage = galleryImages[randomIndex],
			$galleryItem = $(`<a href="${galleryUrl}#${galleryImage}" class="gallery__item"><div class="gallery__image" data-lazy="gallery/thumb/${galleryName}/${galleryImage}"></div></a>`);
		$galleryItem.insertBefore($placeholder);
	}
	$placeholder.remove();
});
